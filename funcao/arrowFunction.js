let dobro = function (a) {
    return 2 * a 
}

dobro = (a) => {
    return 2 * a    //Arrow function
}

dobro = a => 2 * a //Arrow function quando se tem um parâmetro único, normalmente usados numa função que tem uma única linha e executa uma única tarefa.

